sigmoid <- function(x){
  y <- 1/(1+exp(-x))
  return(y)
}

cervix_func <- function(nchain, complete_data, incomplete_data, prop_sd){
  #les arguments data_table sont des matrix
  #ordre : beta0, beta, phi_ij, q et les gamma
  chain <- matrix(NA, nrow = nchain+1, ncol = 7)
  #ordre d, x, w pour complete et d,w sinon
  
  ## PHASE 1: imputation des données
  Ni <- nrow(incomplete_data)
  Nc <- nrow(complete_data)
  N <- Nc + Ni
  
  #1.1 calculs des param des bernouilli
  #ie des probas x_i sachant d_i,w_i
  s <- rep(0, 4) #pour les proba de la loi jointe x,d,w
  s_prime <- rep(0, 4) #pour les proba de la loi jointe d,w
  
  for(i in 1:Nc){
    vals <- complete_data[i,]
    index <- vals[1] + 2*vals[3] + 1
    s[index] <- s[index] + 1*vals[2]
    s_prime[index] <- s_prime[index] + 1
  }
  
  #on utilise s comme vecteur final avec les proba
  s <- s/s_prime
  #1.2 génération des xi
  x <- rep(NA, Ni)
  
  for (i in 1:Ni){
    vals <- incomplete_data[i,]
    index <- vals[1] + 2*vals[2] + 1
    q <- s[index]
    x[i] <- rbinom(1,1,q)
  }
  #on reconstruit nos données
  d <- incomplete_data[,1]
  w <- incomplete_data[,2]
  incomplete_data <- matrix(c(d, x, w), nrow=Ni, ncol=3, byrow=FALSE)
  colnames(incomplete_data) <- c("d", "x", "w")
  
  data_table <- rbind(complete_data, incomplete_data)
  
  #on initialise la chaîne
  chain[1,] <- c(0.5, 0.5, 0.25, 0.25, 0.25, 0.25, 0.5)
  for(iter in 1:nchain){
    beta <- chain[iter, 1:2]
    phis <- chain[iter, 3:6]
    q <- chain[iter, 7]
    
    #MAJ des phi
    nw1 <- 0
    nw0 <- 0
    for (iphi in 1:4){
      for (index in 1:N){
        row <- data_table[index,]
        if (row[3]){
          #on incrémente le compte si la ligne a les xi et di du phi concerné
          nw1 <- nw1 + 1*(row[1]==(iphi+1)%%2)*(row[2]==1*(iphi>=3))
        }else{
          nw0 <- nw0 + 1*(row[1]==(iphi+1)%%2)*(row[2]==1*(iphi>=3))  
          }
      }
      # #on écrit explicitement les produits avec les bernouilli
      # product1 <- chain[iter,2+phi]**nw1 #ici wi=1
      # product0 <- (1-chain[iter,2+phi])**nw0
      update_alpha <- 1 + nw1
      updata_beta <- 1  + nw0 #+ nw1 - nw1 (cf somme des wi=nw1)
      phis[iphi] <- rbeta(1,update_alpha, updata_beta)
    }
    
    #MAJ de q
    sum_x <- sum(data_table[,2])
    update_alpha <- 1 + sum(data_table[,2]) + sum_x
    update_beta <- 1 + N - sum_x
    q <- rbeta(1, update_alpha, updata_beta)
    
    #MAJ des deux betas: par prop de M-H
    acc_rates <- rep(0, 2)
    for (ibeta in 1:2){
      current <- beta[ibeta]
      prop <- rnorm(1, current, prop_sd)
      beta_prop <- beta
      beta_prop[ibeta] <- prop
      
      #on calcule les pi associés à current et à prop
      pi_current <- sigmoid(beta[1] + beta[2]*data_table[,2])
      pi_prop <- sigmoid(beta_prop[1] + beta_prop[2]*data_table[,2])
      
      top <- -(prop^2)/(2*prop_sd^2) + sum(data_table[,1]*log(pi_prop) + (1-data_table[,1])*log(1-pi_prop))
      bottom <- -(current^2)/(2*prop_sd^2) + sum(data_table[,1]*log(pi_current) + (1-data_table[,1])*log(1-pi_current))
      
      alpha <- exp(top - bottom)
      # browser()
      if (runif(1) < alpha)
        beta <- beta_prop
        acc_rates[ibeta] <- acc_rates[ibeta] + 1
    }
    #MAJ de la chaîne
    chain[iter+1,] <- c(beta, phis, q)
  
  }
  #Calcul des gamma à partir de la chaîne
  gamma <- matrix(NA, nchain + 1, 2)
  list_q <- (1-chain[,7])/chain[,7]
  gamma[,1] <- 1/(1 + (1+exp(chain[,1] + chain[,2]))/(1+exp(chain[,1]))*list_q)
  gamma[,2] <- 1/(1 + (1+exp(-chain[,1] - chain[,2]))/(1+exp(-chain[,1]))*list_q)
  chain <- cbind(chain, gamma)
  return(list(chain = chain, acc_rates = acc_rates / nchain))
}
